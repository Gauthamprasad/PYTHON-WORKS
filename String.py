#Strings in python are immutable

# We can simply print a string by using print function

print('Gautham')

# Single and Double cord's in strings

print("Gautham's laptop")           #here we use double cords because inside the string we need to use single cords.

# Special condition

print('Gautham\'s laptop')          #here ( \ ) used to ignore special meaning of single cord.

# Concatenate string

print('Gautham ' + 'Prasad')

print(10 * 'Gautham ')

print('Gautham \n Prasad')          # \n means new line

# Raw string
# Inorder to cancel special meaning of \n we use raw string (r).

print(r'Gautham \n Prasad')